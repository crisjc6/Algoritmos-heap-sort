    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        //JCalendar date = calendario;
        int a침o = calendario.getCalendar().get(Calendar.YEAR);
        int mes = calendario.getCalendar().get(Calendar.MARCH);
        int dia = calendario.getCalendar().get(Calendar.DAY_OF_MONTH);

        String fecha =(a침o+""+mes+""+dia);
               
        if(jTextField1.getText().equals("")||jTextField2.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese todos los datos");
        }else{
            try {
                ingresarTabla(jTextField1.getText(), jTextField2.getText(), Integer.parseInt(fecha));
            } catch (Exception ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            //JOptionPane.showMessageDialog(null, fecha);
            jTextField1.setText("");
            jTextField2.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

